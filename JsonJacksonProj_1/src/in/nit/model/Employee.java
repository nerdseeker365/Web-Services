package in.nit.model;

public class Employee {
	private int edi;
	private String ename;
	private double esal;
	public int getEdi() {
		return edi;
	}
	public void setEdi(int edi) {
		this.edi = edi;
	}
	public String getEname() {
		return ename;
	}
	public void setEname(String ename) {
		this.ename = ename;
	}
	public double getEsal() {
		return esal;
	}
	public void setEsal(double esal) {
		this.esal = esal;
	}
	

}
